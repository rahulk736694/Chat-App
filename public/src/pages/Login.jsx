import React, { useState, useEffect } from "react";
import axios from "axios";
import styled from "styled-components";
import { useNavigate, Link } from "react-router-dom";
import Logo from "../assets/logo.svg";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import { loginRoute } from "../utils/APIRoutes";

export default function Login() {
  const navigate = useNavigate();
  const [values, setValues] = useState({ username: "", password: "" });

  const toastOptions = {
    position: "bottom-right",
    autoClose: 5000,
    pauseOnHover: true,
    draggable: true,
    theme: "dark",
  };

  const LOCAL_STORAGE_KEY = process.env.REACT_APP_LOCALHOST_KEY || "user";

  useEffect(() => {
    if (localStorage.getItem(LOCAL_STORAGE_KEY)) {
      navigate("/");
    }
  }, [navigate, LOCAL_STORAGE_KEY]);

  const handleChange = (event) => {
    setValues({ ...values, [event.target.name]: event.target.value });
  };

  const validateForm = () => {
    const { username, password } = values;
    if (!username || !password) {
      toast.error("Username and Password are required.", toastOptions);
      return false;
    }
    return true;
  };

  const handleSubmit = async (event) => {
    event.preventDefault();
    if (validateForm()) {
      const { username, password } = values;
      try {
        console.log("Attempting login with:", { username, password });
        console.log("Login API URL:", loginRoute);

        const { data } = await axios.post(loginRoute, { username, password });

        console.log("API Response:", data);

        if (!data || typeof data.status === "undefined") {
          toast.error("Unexpected response from server.", toastOptions);
          return;
        }

        if (data.status === false) {
          toast.error(data.msg || "Invalid credentials.", toastOptions);
        } else {
          localStorage.setItem(LOCAL_STORAGE_KEY, JSON.stringify(data.user));
          navigate("/Chat");
        }
      } catch (error) {
        console.error("Login error:", error);
        toast.error("Login failed. Please try again later.", toastOptions);
      }
    }
  };

  return (
    <>
      <FormContainer>
        <form onSubmit={handleSubmit}>
          <div className="brand">
            <img src={Logo} alt="logo" />
            <h1>Snappy</h1>
          </div>
          <input
            type="text"
            placeholder="Username"
            name="username"
            value={values.username}
            onChange={handleChange}
            required
          />
          <input
            type="password"
            placeholder="Password"
            name="password"
            value={values.password}
            onChange={handleChange}
            required
          />
          <button type="submit">Log In</button>
          <span>
            Don't have an account? <Link to="/register">Create One.</Link>
          </span>
        </form>
      </FormContainer>
      <ToastContainer />
    </>
  );
}

